BASE_FOODS = [
    {"name": n, "calories": c, "proteins": p, "fats": f, "carbohydrates": ch}
    for (n, c, p, f, ch) in _RAW
]


def search_base(query: str, limit: int = 20) -> list[dict]:
    """Поиск по встроенной базе: все слова запроса должны встречаться в названии
    (в любом порядке). Возвращает нормализованные записи КБЖУ на 100 г."""
    tokens = [t for t in (query or "").lower().split() if t]
    if not tokens:
        return []
    out = []
    for food in BASE_FOODS:
        name = food["name"].lower()
        if all(tok in name for tok in tokens):
            out.append(food)
            if len(out) >= limit:
                break
    return out
